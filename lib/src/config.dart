/// ## [Config] Class Documentation
///
/// A utility class containing default configuration values for a component.
///
/// Usage:
/// This class provides default width and height values for various components.
///
/// Example:
/// ```dart
/// double width = Config.defaultWidth;
/// double height = Config.defaultHeight;
/// ```
class Config {
  Config._();

  static const double defaultWidth = 100;
  static const double defaultHeight = 10;
}
